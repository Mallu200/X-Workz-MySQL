-- NUMERIC TYPES:
-- INT: For IDs, Years, Counts.
-- DECIMAL(5,2): For Prices (e.g., 999.99).
-- FLOAT/DOUBLE: For scientific calculations.

-- STRING TYPES:
-- CHAR(2): Fixed length (e.g., Country codes like 'US', 'IN').
-- VARCHAR(255): Variable length (e.g., Usernames, Movie Titles).
-- TEXT: For very long descriptions/reviews.

-- DATE TYPES:
-- DATE: YYYY-MM-DD.
-- DATETIME: YYYY-MM-DD HH:MM:SS (e.g., exact time a user clicked 'Play').
-- YEAR: Just the 4-digit year.